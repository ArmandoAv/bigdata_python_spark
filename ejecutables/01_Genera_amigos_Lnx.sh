# Genera archivo de amigos en una red social


# Rango de edades de la red social
min=16
max=65
let rango=$max-$min+1

# Genera un numero aleatorio hasta el 5000
usuario=$(($RANDOM%5000))
# Genera un numero aleatorio hasta el 65
edad=$(($(($RANDOM%$rango))+$min))
# Genera un numero aleatorio hasta el 1000
amigos=$(($RANDOM%1000))
# Pega la informacion en el archivo de temperatura
echo $usuario,Jhon Doe,$edad,$amigos >> ../datos/amigos.csv

